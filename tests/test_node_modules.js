/**
 * Node Module Boundary Test
 *
 * Validates node module edge cases and error paths
 */

var assert = console.assert;
var buffer = require("buffer");
var crypto = require("crypto");
var events = require("events");
var path = require("path");
var os = require("os");
var url = require("url");
var querystring = require("querystring");
var util = require("util");
var string_decoder = require("string_decoder");
var fs = require("fs");
var assert_module = require("assert");

var passed = 0;
var failed = 0;

function check(name, fn) {
    try {
        fn();
        passed++;
    } catch(e) {
        failed++;
        console.log("FAIL: " + name + " — " + e.message);
    }
}

// ── Buffer boundary tests ─────────────────────────────────────────
check("Buffer.alloc(0) returns empty", function() {
    var b = Buffer.alloc(0);
    assert(b.length === 0);
});

check("Buffer.alloc(5) zeroed", function() {
    var b = Buffer.alloc(5);
    assert(b.length === 5);
    for (var i = 0; i < 5; i++) assert(b[i] === 0);
});

check("Buffer.from string", function() {
    var b = Buffer.from("hello");
    assert(b.length === 5);
    assert(b[0] === 104); // 'h'
});

check("Buffer.from hex string", function() {
    var b = Buffer.from("48656c6c6f", "hex");
    assert(b.toString() === "Hello");
});

check("Buffer toString utf8", function() {
    var b = Buffer.from([72, 101, 108, 108, 111]);
    assert(b.toString() === "Hello");
});

check("Buffer concat", function() {
    var a = Buffer.from("foo");
    var c = Buffer.from("bar");
    var r = Buffer.concat([a, c]);
    assert(r.length === 6);
    assert(r.toString() === "foobar");
});

check("Buffer isBuffer", function() {
    assert(Buffer.isBuffer(Buffer.alloc(1)));
    assert(!Buffer.isBuffer("string"));
    assert(!Buffer.isBuffer(42));
});

check("Buffer slice", function() {
    var b = Buffer.from("Hello World");
    var s = b.slice(0, 5);
    assert(s.toString() === "Hello");
});

// ── Crypto boundary tests ─────────────────────────────────────────
check("crypto.createHash md5", function() {
    var h = crypto.createHash("md5");
    h.update("test");
    var digest = h.digest("hex");
    assert(typeof digest === "string");
    assert(digest.length === 32);
});

check("crypto.createHash sha256", function() {
    var h = crypto.createHash("sha256");
    h.update("hello");
    var digest = h.digest("hex");
    assert(digest.length === 64);
});

check("crypto.createHash sha512", function() {
    var h = crypto.createHash("sha512");
    h.update("data");
    var digest = h.digest("hex");
    assert(digest.length === 128);
});

check("crypto.createHmac", function() {
    var h = crypto.createHmac("sha256", "key");
    h.update("message");
    var digest = h.digest("hex");
    assert(typeof digest === "string");
    assert(digest.length === 64);
});

// ── Events boundary tests ─────────────────────────────────────────
check("EventEmitter once", function() {
    var ee = new events.EventEmitter();
    var count = 0;
    ee.once("tick", function() { count++; });
    ee.emit("tick");
    ee.emit("tick");
    assert(count === 1, "once fires only once");
});

check("EventEmitter removeListener", function() {
    var ee = new events.EventEmitter();
    var count = 0;
    var fn = function() { count++; };
    ee.on("x", fn);
    ee.emit("x");
    ee.removeListener("x", fn);
    ee.emit("x");
    assert(count === 1);
});

check("EventEmitter listenerCount", function() {
    var ee = new events.EventEmitter();
    ee.on("a", function() {});
    ee.on("a", function() {});
    assert(ee.listenerCount("a") === 2);
    assert(ee.listenerCount("b") === 0);
});

// ── Path boundary tests ───────────────────────────────────────────
check("path.join multiple", function() {
    assert(path.join("a", "b", "c") === "a/b/c");
});

check("path.dirname", function() {
    assert(path.dirname("/a/b/c.txt") === "/a/b");
});

check("path.basename with ext", function() {
    assert(path.basename("/a/b/c.txt", ".txt") === "c");
});

check("path.extname", function() {
    assert(path.extname("file.tar.gz") === ".gz");
    assert(path.extname("noext") === "");
});

check("path.isAbsolute", function() {
    assert(path.isAbsolute("/usr/bin"));
    assert(!path.isAbsolute("relative/path"));
});

// ── OS boundary tests ─────────────────────────────────────────────
check("os.cpus returns array", function() {
    var cpus = os.cpus();
    assert(Array.isArray(cpus));
    assert(cpus.length > 0);
});

check("os.totalmem is positive", function() {
    assert(os.totalmem() > 0);
});

check("os.freemem is non-negative", function() {
    assert(os.freemem() >= 0);
});

check("os.type is string", function() {
    assert(typeof os.type() === "string");
    assert(os.type().length > 0);
});

// ── URL boundary tests ────────────────────────────────────────────
check("URL parsing", function() {
    var u = new url.URL("https://example.com:8080/path?q=1#hash");
    assert(u.protocol === "https:");
    assert(u.hostname === "example.com");
    assert(u.port === "8080");
    assert(u.pathname === "/path");
    assert(u.search === "?q=1");
    assert(u.hash === "#hash");
});

check("URL searchParams", function() {
    var u = new url.URL("https://x.com?a=1&b=2");
    assert(u.searchParams.get("a") === "1");
    assert(u.searchParams.get("b") === "2");
    assert(u.searchParams.get("c") === null);
});

// ── Querystring boundary tests ────────────────────────────────────
check("querystring parse edge cases", function() {
    var p = querystring.parse("a=1&a=2&empty=&b=3");
    assert(p.a[0] === "1");
    assert(p.a[1] === "2");
    assert(p.empty === "");
    assert(p.b === "3");
});

check("querystring stringify", function() {
    assert(querystring.stringify({a: "1", b: "2"}) === "a=1&b=2");
});

// ── Util boundary tests ───────────────────────────────────────────
check("util.isArray", function() {
    assert(util.isArray([]) === true);
    assert(util.isArray({}) === false);
});

check("util.isString", function() {
    assert(util.isString("hello") === true);
    assert(util.isString(42) === false);
});

// ── StringDecoder boundary tests ──────────────────────────────────
check("StringDecoder basic", function() {
    var sd = new string_decoder.StringDecoder();
    assert(typeof sd.write === "function");
    assert(typeof sd.end === "function");
});

// ── Assert module boundary tests ──────────────────────────────────
check("assert.ok true", function() {
    assert_module.ok(true);
    assert_module.ok(1);
});

check("assert.strictEqual", function() {
    assert_module.strictEqual(1, 1);
    assert_module.strictEqual("a", "a");
});

check("assert.deepStrictEqual", function() {
    assert_module.deepStrictEqual({a: 1}, {a: 1});
    assert_module.deepStrictEqual([1, 2], [1, 2]);
});

// ── Summary ──────────────────────────────────────────────────────
console.log("\n========== Node Module Boundary Test ==========");
console.log("PASSED: " + passed);
console.log("FAILED: " + failed);
console.log("================================================");
console.log(failed === 0 ? "RESULT: ALL PASS" : "RESULT: FAIL");
